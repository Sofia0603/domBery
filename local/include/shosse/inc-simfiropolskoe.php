	</p>
 </li>
	<li>
	<p>
 <b>Природная красота:</b> Половину территории занимают леса и заповедники, рядом — чистые водоемы для рыбалки и купания.
	</p>
 </li>
	<li>
	<p>
 <b>Удобное расположение:</b> Хорошая транспортная доступность, регулярные работы по расширению трасс и улучшению дорожного покрытия.
